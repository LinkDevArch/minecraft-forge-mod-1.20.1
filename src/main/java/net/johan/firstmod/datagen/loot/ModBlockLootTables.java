public class ModBlockLootTables extends BlockLootSubProvider {
    public ModBlockLootTables() {
        super(Set.of(), FeatureFlags.REGISTRY.allFlags());
    }

    @Override
    protected void generate() {
        //this.dropSelf(ModBlocks.DIAMANTIUS_BLOCK.get());

        this.add(ModBlocks.DIAMANTIUS_ORE.get(), block -> createCopperLikeOreDrops(ModBlocks.DIAMANTIUS_ORE.get(),
                ModItems.DIAMANTIUS.get()));

        this.add(ModBlocks.DEEPSLATE_DIAMANTIUS_ORE.get(), block -> createCopperLikeOreDrops(ModBlocks.DEEPSLATE_DIAMANTIUS_ORE.get(),
                ModItems.DIAMANTIUS.get()));
    }

    protected LootTable.Builder createCopperLikeOreDrops(Block pBlock, Item item) {
        return createSilkTouchDispatchTable(pBlock, this.applyExplosionDecay(pBlock,
                LootItem.lootTableItem(item).apply(SetItemCountFunction.setCount(UniformGenerator.between(
                        2.0F, 5.0F))).apply(ApplyBonusCount.addOreBonusCount(Enchantments.BLOCK_FORTUNE))));
    }

    @Override
    protected Iterable<Block> getKnownBlocks() {
        return ModBlocks.BLOCKS.getEntries().stream().map(RegistryObject::get)::iterator;
    }
}
